import React, { PropTypes, Component } from 'react'
import Base from '../../pages/Base';

export default class Posts extends Base {
  render() {
    return (
      <ul>
        {this.props.posts.map((post, i) =>
          <li key={i}>{post.title}</li>
        )}
      </ul>
    )
  }
}

Posts.propTypes = {
  posts: PropTypes.array.isRequired
}